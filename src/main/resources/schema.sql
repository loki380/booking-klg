CREATE TABLE apartment(
    id BIGINT GENERATED BY DEFAULT AS IDENTITY(START WITH 1, INCREMENT BY 1) PRIMARY KEY,
    area VARCHAR(255),
    description VARCHAR(255),
    price NUMERIC(38,2)
);

CREATE TABLE person(
    id BIGINT GENERATED BY DEFAULT AS IDENTITY(START WITH 1, INCREMENT BY 1) PRIMARY KEY,
    name VARCHAR(255)
);

CREATE TABLE booking(
    id BIGINT GENERATED BY DEFAULT AS IDENTITY(START WITH 1, INCREMENT BY 1) PRIMARY KEY,
    cost NUMERIC(38,2),
    dateFrom timestamp(6),
    dateTo timestamp(6),
    landlord_id bigint,
    tenant_id bigint
);